// == compara se é igual
// === compara se é estritamente igual (inclusive number x string)
// != compara se é diferente
// !== compara se é estritamente diferente (inclusive number x string)

console.log('1º: ' , '1' == 1) //apesar de um ser string e outro number, o JS entende que são iguais
console.log('2º: ' , '1' === 1) //aqui o JS já identificou que um é string e o outro number
console.log('3º: ' , '1' != 1)
console.log('4º: ' , '1' !== 1)
console.log('5º: ' , 1 > 2)
console.log('6º: ' , 1 < 1)
console.log('7º: ' , 1 <= 2)
console.log('8º: ' , 1 >= 2)